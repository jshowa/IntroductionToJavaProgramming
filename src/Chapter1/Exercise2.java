package Chapter1;

/**
 * Prints the message "Welcome to Java!" 5 times to stdout using Sys.out and
 * for loop
 * @author jshowa
 */
public class Exercise2 {
    public static void main(String[] args) {
        System.out.println("Welcome to Java!");
        System.out.println("Welcome to Java!");
        System.out.println("Welcome to Java!");
        System.out.println("Welcome to Java!");
        System.out.println("Welcome to Java!");
        
        for (int i = 0; i <= 4; i++) {
            System.out.println("Welcome to Java!");
        }
    }
    
}
